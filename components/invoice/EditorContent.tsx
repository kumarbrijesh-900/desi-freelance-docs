function getNextStep(step: InvoiceStepperStep) {
  const currentIndex = orderedSteps.indexOf(step);

  if (currentIndex === -1 || currentIndex === orderedSteps.length - 1) {
    return null;
  }

  return orderedSteps[currentIndex + 1];
}

function InlineStepSection({
  step,
  isActive,
  isCompleted,
  issueCount,
  optionalIssueCount,
  onActivate,
  children,
  footer,
}: {
  step: InvoiceStepperStep;
  isActive: boolean;
  isCompleted: boolean;
  issueCount: number;
  optionalIssueCount: number;
  onActivate: () => void;
  children: ReactNode;
  footer?: ReactNode;
}) {
  const [isMounted, setIsMounted] = useState(false);
  const stepNumber = orderedSteps.indexOf(step) + 1;
  const stepLabel = getStepShortLabel(step);
  const detailCopy = getStepDescription(step);
  const stepKind = getStepKind(step);
  let statusLabel =
    !isCompleted && isMounted && issueCount > 0
      ? `${issueCount} mandatory`
      : isCompleted
        ? optionalIssueCount > 0 ? "Ready" : "Complete (ok)"
        : isActive && isMounted && issueCount === 0
          ? optionalIssueCount > 0 ? "Ready" : "Complete (ok)"
          : isActive
            ? "In progress"
            : "Pending";

  if (step === "totals" && isActive && !isCompleted) {
    statusLabel = "Pending";
  }

  const StepIcon = {
    agency: Building2,
    client: Users,
    deliverables: ListChecks,
    payment: CreditCard,
    meta: Settings2,
    totals: ShieldCheck,
  }[step];

  useEffect(() => {
    setIsMounted(true);
  }, []);

  return (
    <motion.section
      layout
      id={`step-${step}`}
      data-step-section={step}
      data-step-state={
        isActive ? "active" : isCompleted ? "completed" : "incomplete"
      }
      data-step-kind={stepKind}
      className={cn(
        "invoice-step-card relative scroll-mt-32 overflow-visible rounded-[18px] px-[18px] py-4 transition-[background-color,border-color,box-shadow] duration-[var(--app-duration-medium)] sm:px-5",
      )}
    >
      <div className="flex flex-col gap-2">
        <div className="flex flex-col gap-2 md:flex-row md:items-start md:justify-between">
          <button
            type="button"
            onClick={onActivate}
            data-step-activator={step}
            className="min-w-0 flex-1 text-left"
          >
            <div className="flex items-start gap-3">
              <span
                className={cn(
                  "invoice-step-dot mt-[9px] inline-flex h-2.5 w-2.5 shrink-0 rounded-full",
                  isCompleted
                    ? "bg-[color:var(--interactive-secondary)]"
                    : isActive
                      ? "bg-[color:var(--interactive-primary)]"
                      : "bg-[color:var(--border-strong)]",
                )}
              />
              <div className="min-w-0">
                <p className="text-[10px] font-semibold uppercase tracking-[0.16em] text-[color:var(--text-muted)]">
                  Step {stepNumber}
                </p>
                <h2 className="mt-1 text-[19px] font-semibold tracking-[-0.024em] text-[color:var(--text-primary)]">
                  <div className="flex items-center gap-2">
                    {StepIcon && (
                      <StepIcon
                        className={cn(
                          "w-5 h-5 shrink-0 transition-colors duration-200",
                          isActive || isCompleted
                            ? "text-inherit"
                            : "text-slate-400",
                        )}
                        strokeWidth={1.5}
                      />
                    )}
                    {stepLabel}
                  </div>
                </h2>
                <p className="mt-1 max-w-2xl text-[11px] leading-5 text-[color:var(--text-muted)]">
                  {detailCopy}
                </p>
              </div>
            </div>
          </button>

          <div className="flex shrink-0 flex-wrap items-center gap-2">
            <span
              className={getAppStatusPillClass(
                isCompleted ? "success" : issueCount > 0 ? "warning" : isActive ? "default" : "muted",
              )}
            >
              {statusLabel}
            </span>
          </div>
        </div>

        <motion.div
          layout
          initial={false}
          className="invoice-step-divider pt-2"
        >
          <div className={cn("space-y-4", footer ? "mb-20" : "mb-4")}>
            {children}
            {footer}
          </div>
        </motion.div>
      </div>
    </motion.section>
  );
}

